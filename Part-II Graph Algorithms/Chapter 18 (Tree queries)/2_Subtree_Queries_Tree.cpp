class SubTreeQuery
{
	public:
		vector<vector<int>> graph;
		int v, e, head;
	public:
		SubTreeQuery()
		{
			cin>>this->v>>this->e>>this->head;
			graph.resize(v, vector<int>());
			for(int edge=0; edge < this->e; edge++)
			{
				int start, end;
				cin>>start>>end;
				graph[start].push_back(end);
			}
		}
		
};

